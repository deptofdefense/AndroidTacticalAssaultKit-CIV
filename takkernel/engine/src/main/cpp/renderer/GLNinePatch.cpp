        int64_t GLNinePatch::loadTextureBitmap(GLTextureAtlas2 *atlas, Size s)
        {
            Bitmap *b;
            const char *name;
            switch (s) {
            case SMALL:
                name = "NinepatchSmall";
                b = &SMALL_BITMAP;
                break;
            default:
            case MEDIUM:
                name = "NinepatchMedium";
                b = &MEDIUM_BITMAP;
                break;
            case LARGE:
                name = "NinepatchLarge";
                b = &LARGE_BITMAP;
                break;
            }

            Bitmap2::DataPtr data((uint8_t *)b->data, Memory_leaker_const<uint8_t>);
            Bitmap2::Format format;
            GLTexture2_getBitmapFormat(&format, b->format, b->dataType);
            std::size_t width = b->width;
            std::size_t height = b->height;

            int64_t key = 0LL;
            atlas->addImage(&key, name, Bitmap2(std::move(data), width, height, format));
            return key;
        }



        /************************************************************************/
        // Vertex computation


        void GLNinePatch::buildPatchVerts(float *verts, int numVertsPerCorner,
                                          float radius, double radiansPerVert,
                                          float x, float y, float z, float width, float height)
        {
            float *pVerts = verts;

            const int limit = (numVertsPerCorner * 4);
            float tx = 0.0f;
            float ty = 0.0f;
            for (int i = 0; i < limit; i++) {
                if ((i%numVertsPerCorner) == 0) {
                    switch (i / numVertsPerCorner) {
                    case 0:
                        tx = width - (radius*1.0f);
                        ty = height - (radius*1.0f);
                        break;
                    case 1:
                        tx = radius;
                        ty = height - (radius*1.0f);
                        break;
                    case 2:
                        tx = radius;
                        ty = radius;
                        break;
                    case 3:
                        tx = width - (radius*1.0f);
                        ty = radius;
                        break;
                    default:
                        // XXX - illegal state
                        break;
                    }
                }
                (*pVerts++) = x + tx + (radius*(float)cos(radiansPerVert*i));
                (*pVerts++) = y + ty + (radius*(float)sin(radiansPerVert*i));
                (*pVerts++) = z;
            }

            (*pVerts++) = verts[0];
            (*pVerts++) = verts[1];
            (*pVerts++) = z;

        }

        
        void GLNinePatch::fillCoordsBuffer(const std::size_t size,
                                           float texPatchCol0, float texPatchRow0,
                                           float texPatchCol1, float texPatchRow1,
                                           float texPatchCol2, float texPatchRow2,
                                           float texPatchCol3, float texPatchRow3,
                                           float z,
                                           float *buffer)
        {
            float *coordBuffer = buffer;

            float texPatchRows[4];
            float texPatchCols[4];

            texPatchRows[0] = texPatchRow0;
            texPatchRows[1] = texPatchRow1;
            texPatchRows[2] = texPatchRow2;
            texPatchRows[3] = texPatchRow3;

            texPatchCols[0] = texPatchCol0;
            texPatchCols[1] = texPatchCol1;
            texPatchCols[2] = texPatchCol2;
            texPatchCols[3] = texPatchCol3;

            for (int i = 0; i < 4; i++) {
                for (int j = 0; j < 4; j++) {
                    *coordBuffer++ = texPatchCols[j];
                    *coordBuffer++ = texPatchRows[i];
                    if(size == 3u)
                        *coordBuffer++ = z;
                }
            }
        }




        /************************************************************************/
        // Private API - batch and draw methods

        void GLNinePatch::batchTex(GLRenderBatch2& batch, float x, float y, float z,
            float width, float height, float r,
            float g, float b, float a)
        {
            if (last_texture_x_ != x ||
                last_texture_y_ != y ||
                last_texture_z_ != z ||
                last_texture_width_ != width ||
                last_texture_height_ != height) {

                float scalingWidth = width - (x0_ + (texture_data_width_ - x1_));
                float scalingHeight = height - (y0_ + (texture_data_height_ - y1_));

                fillCoordsBuffer(3u,
                    x + 0.0f, y + 0.0f,
                    x + x0_, y + y0_,
                    x + x0_ + scalingWidth, y + y0_ + scalingHeight,
                    x + width, y + height,
                    z,
                    tex_verts_);

                last_texture_x_ = x;
                last_texture_y_ = y;
                last_texture_z_ = z,
                last_texture_width_ = width;
                last_texture_height_ = height;
            }

            batch.batch(texture_id_,
                GL_TRIANGLES,
                16u,
                3u,
                0, tex_verts_,
                0u, tex_coords_,
                TEX_INDICES_COUNT, reinterpret_cast<const unsigned short*>(TEX_INDICES),
                r, g, b, a);
        }


        void GLNinePatch::batchFan(GLRenderBatch2& batch, float x, float y, float z,
            float width, float height, float r,
            float g, float b, float a)
        {
            if (last_triangle_x_ != x ||
                last_triangle_y_ != y ||
                last_triangle_z_ != z || 
                last_triangle_width_ != width ||
                last_triangle_height_ != height) {

                buildPatchVerts(triangle_verts_, NUM_VERTS_PER_CORNER, radius_,
                    RADIANS_PER_VERT, x, y, z, width, height);
                last_triangle_x_ = x;
                last_triangle_y_ = y;
                last_triangle_z_ = z;
                last_triangle_width_ = width;
                last_triangle_height_ = height;
            }

            batch.batch(-1,
                GL_TRIANGLE_FAN,
                NUM_VERTS_PER_PATCH,
                3u,
                12u, triangle_verts_,
                12u, nullptr,
                r, g, b, a);
        }

        void GLNinePatch::drawTriangleFan(float x, float y, float z,
                                          float width, float height)
        {
            GLES20FixedPipeline *fixedPipe = GLES20FixedPipeline::getInstance();

            if (last_triangle_x_ != x ||
                last_triangle_y_ != y ||
                last_triangle_z_ != z ||
                last_triangle_width_ != width ||
                last_triangle_height_ != height) {

                buildPatchVerts(triangle_verts_, NUM_VERTS_PER_CORNER, radius_,
                                RADIANS_PER_VERT, x, y, z, width, height);
                last_triangle_x_ = x;
                last_triangle_y_ = y;
                last_triangle_z_ = z;
                last_triangle_width_ = width;
                last_triangle_height_ = height;
            }

            // enable all the state
            fixedPipe->glEnableClientState(GLES20FixedPipeline::ClientState::CS_GL_VERTEX_ARRAY);
            glEnable(GL_BLEND);

            glBlendFunc(GL_SRC_ALPHA, GL_ONE_MINUS_SRC_ALPHA);

            fixedPipe->glVertexPointer(3, GL_FLOAT, 0, triangle_verts_);

            fixedPipe->glDrawArrays(GL_TRIANGLE_FAN, 0, NUM_VERTS_PER_PATCH);

            fixedPipe->glDisableClientState(GLES20FixedPipeline::ClientState::CS_GL_VERTEX_ARRAY);
            glDisable(GL_BLEND);
        }


        void GLNinePatch::drawTexture(float x, float y, float z, float width, float height)
        {
            if (last_texture_x_ != x ||
                last_texture_y_ != y ||
                last_texture_z_ != z ||
                last_texture_width_ != width ||
                last_texture_height_ != height) {

                float scalingWidth = width - (x0_ + (texture_data_width_ - x1_));
                float scalingHeight = height - (y0_ + (texture_data_height_ - y1_));

                fillCoordsBuffer(3u,
                                 x + 0.0f, y + 0.0f,
                                 x + x0_, y + y0_,
                                 x + x0_ + scalingWidth, y + y0_ + scalingHeight,
                                 x + width, y + height,
                                 z,
                                 tex_verts_);

                last_texture_x_ = x;
                last_texture_y_ = y;
                last_texture_z_ = z;
                last_texture_width_ = width;
                last_texture_height_ = height;
            }

            GLES20FixedPipeline *fixedPipe = GLES20FixedPipeline::getInstance();

            // enable all the state
            glBindTexture(GL_TEXTURE_2D, texture_id_);

            fixedPipe->glEnableClientState(GLES20FixedPipeline::ClientState::CS_GL_VERTEX_ARRAY);
            fixedPipe->glEnableClientState(GLES20FixedPipeline::ClientState::CS_GL_TEXTURE_COORD_ARRAY);

            glEnable(GL_BLEND);
            glBlendFunc(GL_SRC_ALPHA, GL_ONE_MINUS_SRC_ALPHA);

            fixedPipe->glVertexPointer(3, GL_FLOAT, 0, tex_verts_);
            fixedPipe->glTexCoordPointer(2, GL_FLOAT, 0, tex_coords_);

            fixedPipe->glDrawElements(GL_TRIANGLES, 54, GL_UNSIGNED_SHORT, (const char *)TEX_INDICES);

            fixedPipe->glDisableClientState(GLES20FixedPipeline::ClientState::CS_GL_TEXTURE_COORD_ARRAY);
            fixedPipe->glDisableClientState(GLES20FixedPipeline::ClientState::CS_GL_VERTEX_ARRAY);

            glDisable(GL_BLEND);
        }


