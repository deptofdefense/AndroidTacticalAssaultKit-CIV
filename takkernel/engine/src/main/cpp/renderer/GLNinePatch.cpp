        void GLNinePatch::batch(GLRenderBatch *legacy, float x, float y,
                                float width, float height,
                                float r, float g, float b, float a)
        {
            if (!legacy)
                return;

            GLRenderBatch2 &batch = GLRenderBatch_adapt(*legacy);
            this->batch(batch, x, y, 0.f, width, height, r, g, b, a);
        }

        void GLNinePatch::batch(TAK::Engine::Renderer::GLRenderBatch2& batch, float x, float y,
            float width, float height,
            float r, float g, float b, float a)
        {
            this->batch(batch, x, y, 0.f, width, height, r, g, b, a);
        }
        void GLNinePatch::batch(TAK::Engine::Renderer::GLRenderBatch2& batch, float x, float y, float z,
            float width, float height,
            float r, float g, float b, float a)
        {
            if (texture_id_ == 0 && texture_atlas_ != nullptr && tex_coords_ == nullptr)
                validateTextureEntry();
            if (texture_id_ != 0)
                batchTex(batch, x, y, z, width, height, r, g, b, a);
            else
                batchFan(batch, x, y, z, width, height, r, g, b, a);
        }

        void GLNinePatch::draw(float width, float height)
        {
            draw(0, 0, width, height, false);
        }

        void GLNinePatch::draw(float x, float y,
                               float width, float height, bool textured)
        {
            if (textured && texture_atlas_ != nullptr && tex_coords_ == nullptr)
                validateTextureEntry();

            if (textured && texture_id_ != 0) {
                drawTexture(x, y, 0.f, width, height);
            } else
                drawTriangleFan(x, y, 0.f, width, height);
        }




