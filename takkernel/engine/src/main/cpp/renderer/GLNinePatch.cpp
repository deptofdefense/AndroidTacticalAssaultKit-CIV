            Bitmap scaleNearestNeighbor(Bitmap b, int w, int h)
            {
                Bitmap ret;
                ret.width = w;
                ret.height = h;
                ret.dataType = b.dataType;
                ret.format = b.format;
                ret.dataLen = w * h * 4;
                char *data = new char[ret.dataLen];
                ret.releaseData = scaledRelease;
                ret.getScaledInstance = scaleNearestNeighbor;
                ret.data = data;

                char *sdata = (char *)b.data;

                if (!w || !h)
                    return ret;

                double xr = b.width / (double)w;
                double yr = b.height / (double)h;
                for (int y = 0; y < h; ++y) {
                    char *rrow = data + y * w * 4;
                    for (int x = 0; x < w; ++x) {
                        double px = floor(x * xr);
                        double py = floor(y * yr);
                        char *sloc = sdata + ((int)((py * b.width) + px));
                        memcpy(rrow, sloc, 4);
                        rrow += 4;
                    }
                }
                return ret;
            }

