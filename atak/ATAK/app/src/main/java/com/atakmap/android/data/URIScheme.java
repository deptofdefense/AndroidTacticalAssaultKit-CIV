
package com.atakmap.android.data;

/**
 * Some commonly used URI schemes used within the URI content framework
 */
public class URIScheme {

    // File - file://<path>
    public static final String FILE = "file://";

    // ZIP file - zip://<path>
    public static final String ZIP = "zip://";

    // Map item - map-item://<UID>
    public static final String MAP_ITEM = "map-item://";

    // Video alias - video://<UID>
    public static final String VIDEO = "video://";

    // Mission Package manifest - mpm://<path>/<XML base64>
    public static final String MPM = "mpm://";
}
