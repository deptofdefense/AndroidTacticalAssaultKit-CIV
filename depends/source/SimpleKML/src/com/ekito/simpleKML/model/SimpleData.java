package com.ekito.simpleKML.model;

import org.simpleframework.xml.Attribute;
import org.simpleframework.xml.Text;

/**
 * This element assigns a value to the custom data field identified by the name attribute. The type and name of this custom data field are declared in the {@link Schema} element.
 */
public class SimpleData {

    /** The name. */
    @Attribute(required=false)
    private String name;
    
    /** The value. */
    //BYOUNG Bugzilla #1825 - make value optional
    @Text(required=false)
    private String value;

    /**
     * Gets the name.
     *
     * @return the name
     */
    public String getName() {
        return name;
    }

    /**
     * Sets the name.
     *
     * @param name the new name
     */
    public void setName(String name) {
        this.name = name;
    }

    /**
     * Gets the value.
     *
     * @return the value
     */
    public String getValue() {
        return value;
    }

    /**
     * Sets the value.
     *
     * @param value the new value
     */
    public void setValue(String value) {
        this.value = value;
    }
}
